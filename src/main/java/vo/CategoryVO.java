package vo;

public class CategoryVO {
	private int category_ct, category_loc_idx;
	private String category_sports, category_loc;
	
	public int getCategory_ct() {
		return category_ct;
	}
	public void setCategory_ct(int category_ct) {
		this.category_ct = category_ct;
	}
	public int getCategory_loc_idx() {
		return category_loc_idx;
	}
	public void setCategory_loc_idx(int category_loc_idx) {
		this.category_loc_idx = category_loc_idx;
	}
	public String getCategory_sports() {
		return category_sports;
	}
	public void setCategory_sports(String category_sports) {
		this.category_sports = category_sports;
	}
	public String getCategory_loc() {
		return category_loc;
	}
	public void setCategory_loc(String category_loc) {
		this.category_loc = category_loc;
	}
	
}
